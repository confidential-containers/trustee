//! Admin module for managing admin access to the KBS server.
//!
//! This modules has two parts:
//! - authentication: verifies and authenticates token from the request
//! - authorization: checks if the token has access to the given path
//!
//! The workflow is as follows:
//! 1. The authentication module gets the jwt token from the request header.
//! 2. The authentication module verifies the jwt token using the pre-configured public keys and returns the claims.
//! 3. The authorization checks if the token has access to the given path.
//! 4. If the token has access to the given path, the request is allowed.
//! 5. If the token does not have access to the given path, the request is denied.

use actix_web::HttpRequest;
use serde::{Deserialize, Serialize};
use tracing::info;

pub mod authentication;
pub mod authorization;

pub mod error;
pub use error::*;

use crate::admin::authentication::AuthenticationType;
pub use crate::admin::{
    authentication::TokenVerifier,
    authorization::{Authorization, AuthorizerType},
};

/// Claims are the abstraction of the claims generated by the token verifier.
#[derive(Clone, Debug, Default, Serialize, Deserialize, PartialEq)]
pub struct Claims {
    pub role: String,
}

pub struct AuthorizationDecision {
    pub allowed: bool,
    pub reason: String,
}

#[derive(Clone)]
pub enum Admin {
    InsecureAllowAll,
    DenyAll,
    AuthenticatedAuthorization(TokenVerifier, Authorization),
}

#[derive(Clone, Debug, Deserialize, PartialEq)]
#[serde(tag = "authorization_mode", deny_unknown_fields)]
pub enum AdminConfig {
    InsecureAllowAll {},
    DenyAll {},
    AuthenticatedAuthorization {
        authentication: AuthenticationType,
        authorization: AuthorizerType,
    },
}

impl Default for AdminConfig {
    fn default() -> Self {
        Self::InsecureAllowAll {}
    }
}

impl AdminConfig {
    fn validate(&self) -> Result<()> {
        let AdminConfig::AuthenticatedAuthorization {
            authentication: AuthenticationType::BearerJwt(config),
            authorization: _,
        } = self
        else {
            return Ok(());
        };

        if config.identity_providers.is_empty() {
            return Err(Error::InvalidAdminConfig {
                message: "AuthenticatedAuthorization requires at least one `identity_providers` entry in [admin.authentication.bearer_jwt]".into(),
            });
        }

        for (index, idp) in config.identity_providers.iter().enumerate() {
            if idp.public_key_uri.is_none() && idp.jwk_set_uri.is_none() {
                return Err(Error::InvalidAdminConfig {
                    message: format!(
                        "identity_providers[{index}] must set `public_key_uri` and/or `jwk_set_uri`"
                    ),
                });
            }
        }

        Ok(())
    }
}

impl Admin {
    pub async fn new(config: AdminConfig) -> Result<Self> {
        config.validate()?;

        match config {
            AdminConfig::InsecureAllowAll {} => Ok(Admin::InsecureAllowAll),
            AdminConfig::DenyAll {} => Ok(Admin::DenyAll),
            AdminConfig::AuthenticatedAuthorization {
                authentication,
                authorization,
            } => Ok(Admin::AuthenticatedAuthorization(
                TokenVerifier::new(authentication).await?,
                authorization.try_into()?,
            )),
        }
    }

    pub fn check_admin_access(&self, request: &HttpRequest) -> Result<()> {
        match self {
            Admin::InsecureAllowAll => {
                info!("Admin access granted as AllowAll authorization_mode is enabled");
                Ok(())
            }
            Admin::DenyAll => Err(Error::AdminAccessDenied {
                reason: "Admin access denied as DenyAll authorization_mode is enabled".to_string(),
            }),
            Admin::AuthenticatedAuthorization(authentication, authorization) => {
                let claims = authentication.parse_and_verify(request)?;
                info!(
                    "Admin access granted for {} with claims: {:?}",
                    claims.role, claims
                );
                let decision = authorization.authorize(claims, request)?;
                if decision.allowed {
                    Ok(())
                } else {
                    Err(Error::AdminAccessDenied {
                        reason: decision.reason,
                    })
                }
            }
        }
    }
}

