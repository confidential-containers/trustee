    fn make_test_admin_keypair() -> (String, PathBuf) {
        let keypair = PKey::generate_ed25519().expect("generate test ed25519 keypair");
        let private_key_pem = String::from_utf8(
            keypair
                .private_key_to_pem_pkcs8()
                .expect("export test private key"),
        )
        .expect("test private key is valid utf8");
        let public_key_pem = keypair.public_key_to_pem().expect("export test public key");

        let now_nanos = SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .expect("system time before unix epoch")
            .as_nanos();
        let public_key_path =
            std::env::temp_dir().join(format!("kbs-admin-test-pubkey-{now_nanos}.pem"));
        fs::write(&public_key_path, public_key_pem).expect("write test admin public key");

        (private_key_pem, public_key_path)
    }

    #[rstest]
    #[case(
        "/kbs/v0/admin/allowed/path",
        "oidc-1",
        &["example1.io", "example2.io"],
        "oidc-1",
        "example1.io",
        true
    )]
    #[case(
        "/kbs/v0/admin/allowed/path",
        "oidc-2",
        &["example1.io", "example2.io"],
        "oidc-1",
        "example1.io",
        false
    )]
    #[case(
        "/kbs/v0/admin/allowed/path",
        "oidc-1",
        &["wrong.example.io"],
        "oidc-1",
        "example.io",
        false
    )]
    #[case(
        "/kbs/v0/admin/denied/path",
        "oidc-1",
        &["example1.io", "example2.io"],
        "oidc-1",
        "example1.io",
        false
    )]
    #[tokio::test]
    pub async fn check_requests_acl(
        #[case] request_uri: &str,
        #[case] token_issuer: &str,
        #[case] token_audience: &[&str],
        #[case] expected_issuer: &str,
        #[case] expected_audience: &str,
        #[case] expected_allowed: bool,
    ) {
        let (private_key_pem, public_key_path) = make_test_admin_keypair();

        let now = SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .expect("system time before unix epoch")
            .as_secs();
        let claims = json!({
            "iss": token_issuer,
            "role": "Alice",
            "aud": token_audience,
            "iat": now,
            "exp": now + 300,
        });
        let token = encode(
            &Header::new(Algorithm::EdDSA),
            &claims,
            &EncodingKey::from_ed_pem(private_key_pem.as_bytes()).expect("load test private key"),
        )
        .expect("sign test admin token");
        let admin_config_json = json!({
            "authorization_mode": "AuthenticatedAuthorization",
            "authorization": {
                "regex_acl": {
                    "acls": [{
                        "role": "Alice",
                        "allowed_endpoints": "^/kbs/v0/admin/allowed/.+$"
                    }]
                }
            },
            "authentication": {
                "bearer_jwt": {
                    "identity_providers": [{
                        "issuer": expected_issuer,
                        "audience": expected_audience,
                        "public_key_uri": public_key_path
                    }]
                }
            }
        });

        let config: AdminConfig = serde_json::from_value(admin_config_json).unwrap();
        let admin = Admin::new(config).await.unwrap();

        let req = actix_web::test::TestRequest::post()
            .uri(request_uri)
            .insert_header(("Authorization", format!("Bearer {token}")))
            .to_http_request();

        let result = admin.check_admin_access(&req);
        if expected_allowed {
            assert!(result.is_ok(), "request should be allowed by ACL");
        } else {
            let err = result.unwrap_err();
            match err {
                Error::AdminAccessDenied { reason } => assert!(
                    reason.contains("not allowed")
                        || reason.contains("Cannot verify token with any of the issuers")
                ),
                e => panic!("expected ACL rejection, got: {e}"),
            }
        }
    }
